package day17_forLoop;

public class C01_SonsuzDöngü {
    public static void main(String[] args) {

        //Bu kodu çalıştırırsak sonsuz döngüye girer
        // ve RAM dolup bilgisayarı kilitleyene kadar çalışır

        for (int i = 0; i >=0 ; i++) {
            System.out.print(i);
        }

    }
}
